#include"libft.h"

int	ft_isalnum(int c)
{
	if ('0' > c || '9' < c)
	{
		if ('A' > c || 'Z' < c)
		{
			if ('a' > c || 'z' < c)
				return (0);
		}
	}
	return (1);
}
